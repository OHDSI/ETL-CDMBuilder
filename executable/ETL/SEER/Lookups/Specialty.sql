{base},
Standard as (
SELECT DISTINCT 
CASE
WHEN LEN(SOURCE_CODE) = 1 THEN '0' + SOURCE_CODE
ELSE SOURCE_CODE
END as SOURCE_CODE, TARGET_CONCEPT_ID, TARGET_DOMAIN_ID, SOURCE_VALID_START_DATE, SOURCE_VALID_END_DATE
FROM Source_to_Standard
WHERE lower(SOURCE_VOCABULARY_ID) IN ('specialty')
AND lower(TARGET_VOCABULARY_ID) IN ('specialty')

)

select distinct Standard.*
from Standard